import mongoose from 'mongoose';

const postSchemaModule = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0,
    },
    createdAt: {
        type: Date,
        default: new Date(),
    },
})

var PostSchema = mongoose.model('PostSchema', postSchemaModule);

export default PostSchema;